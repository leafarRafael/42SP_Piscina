#include <unistd.h>
#include <stdlib.h>

void	ft_putchar(char c)
{
	write (1, &c, 1);
}

void	ft_putnbr(int nb)
{
	long	nbr;

	nbr = (long)nb;
	if (nbr < 0)
	{
		ft_putchar('-');
		nbr = -nbr;
	}
	if (nbr < 10)
		ft_putchar(nbr + '0');
	if (nbr > 9)
	{
		ft_putnbr(nbr / 10);
		ft_putnbr(nbr % 10);
	}
}


int	main(void)
{
	int *nbr;
	int n;
	n = 9;

	nbr = malloc((n+1) * sizeof(int));

	while (nbr[n] <= 9)
	{

		nbr[n]++;
	}
	return (0);
}

