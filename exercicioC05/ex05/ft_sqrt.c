int ft_sqrt(int nb)
{
    int i;
    int nbr;

    i = 3;
    nbr = 0;
    while (i-- >=0)
        nbr = nb / 2;

    return (nbr-1);
}

#include <stdio.h>
int main(void)
{
	printf("%d",ft_sqrt(36));
	printf("\n\n");
}