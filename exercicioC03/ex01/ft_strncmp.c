int     ft_strncmp(char *s1, char *s2, unsigned int n)
{
    unsigned int     i;

    if (n <= 0)
        return (0);

    i = 0;
    while ((s1[i] != '\0' || s2[i] != '\0') && i < n)
    {
        if ((s1[i]-s2[i]) < 0)
        {
            return (-1);
            break ;
        }
        else if ((s1[i]-s2[i]) > 0)
            return (1);
        i++;
    }
    return (0);
}